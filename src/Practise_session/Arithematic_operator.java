package Practise_session;

public class Arithematic_operator {

	public static void main(String[] args) {
		int a=12;
		boolean b=true;
		int c=10;
		int d = 12,e=12;
		
		System.out.println("~ output is "+~a); //syntax (~a)= (-a-1)
		System.out.println("! output is "+!b); //syntax (!b)= (opposite of b) 
		System.out.println("--a output is "+ --a);//syntax (--a)=(decreasing value of a by 1 in output)
		System.out.println("c-- output is "+ c--);//syntax (c--)=(printing value as it is and saving decrement in server)
		System.out.println("++d output is " + ++d);//syntax (++d)=(increasing value of a by 1 in output)
		System.out.println("e++ output is " + e++);//syntax (e++)=(printing value as it is and saving increment in server)
		System.out.println(~a);
		System.out.println(~a);
		System.out.println(~a);
		System.out.println(~a);
		System.out.println(~a);
		System.out.println(~a);
		System.out.println(~a);
		
	}

}
